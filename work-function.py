#คำสั่งหาพื้นที่สี่เหลี่ยม
def square(width,height):
    ans = width*height
    print(ans)
square(3,5)
#คำสั่งหาพื้นที่สามเหลี่ยม
def triangle(base,height):
    ans = (base*height)/2
    print(ans)
triangle(4,6)
#คำสั่งหาพื้นที่วงกลม
def circle(radius):
    ans = (3.14*radius*radius)
    print(ans)
circle(9)
